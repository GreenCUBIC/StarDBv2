<h2> Translatome plasticity of neocortical astroglia throughout the estrous cycle (2022) </h2>
<div class="m-[20px]">
  <b>Authors:</b>
  Gareth M. Rurak, Chase Groulx, Ashley McFee, Argel Aguilar-Valles, François Charih, James R. Green, Barbara Woodside, Gianfilippo Coppola, Natalina Salmaso
</div>

<div class="m-[20px]">
  <p>
    This database contains TRAPSeq data collected throughout the mouse estrous cycle. Measurements are available for three phases of the estrous cycle: metestrus, proestrus, and estrus. Values for
    male are also accessible.
  </p>
</div>

