<h2> Sex differences in developmental patterns of neocortical astroglia: a mouse translatome database (2022) </h2>
<div class="m-[20px]">
  <b>Authors:</b>
  Gareth M. Rurak, Stephanie Simard, Moises Freitas-Andrade, Baptiste Lacoste, François Charih, Amanda Van Geel, John Stead, Barbara Woodside, James R. Green, Gianfilippo Coppola, Natalina Salmaso
</div>

<div class="m-[20px]">
  <p> This database contains a large quantity of TRAPseq measurements quantifying protein translation in a mouse model at various phases of development (P1, P4, P7, P15, P35 and adulthood). </p>
  <p>
    The scientific value of our database is demonstrated in the accompanying paper
    <a href="https://www.cell.com/cell-reports/fulltext/S2211-1247(22)00022-5">
      <i> Sex differences in developmental patterns of neocortical astroglia: A mouse translatome database </i>
    </a>
    where this rich dataset allowed for the identification of phenotypal differences between sexes.
  </p>

